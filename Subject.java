package subject;

public class Subject {
	private String SubNum;
	private String SubClass;
	private String SubDep;
	private String SubName;
	private String SubPoint;
	private String SubTime;
	private String SubPro;
	private String SubMax;
	public String getSubNum() {
		return SubNum;
	}
	public void setSubNum(String subNum) {
		SubNum = subNum;
	}
	public String getSubClass() {
		return SubClass;
	}
	public void setSubClass(String subClass) {
		SubClass = subClass;
	}
	public String getSubDep() {
		return SubDep;
	}
	public void setSubDep(String subDep) {
		SubDep = subDep;
	}
	public String getSubName() {
		return SubName;
	}
	public void setSubName(String subName) {
		SubName = subName;
	}
	public String getSubPoint() {
		return SubPoint;
	}
	public void setSubPoint(String subPoint) {
		SubPoint = subPoint;
	}
	public String getSubTime() {
		return SubTime;
	}
	public void setSubTime(String subTime) {
		SubTime = subTime;
	}
	public String getSubPro() {
		return SubPro;
	}
	public void setSubPro(String subPro) {
		SubPro = subPro;
	}
	public String getSubMax() {
		return SubMax;
	}
	public void setSubMax(String subMax) {
		SubMax = subMax;
	}
	
}
